const I18nContext = createContext(null);

function mergeDictionaries(base, overrides) {
  const output = { ...base };
  Object.entries(overrides || {}).forEach(([key, value]) => {
    if (
      value &&
      typeof value === "object" &&
      !Array.isArray(value) &&
      base?.[key] &&
      typeof base[key] === "object" &&
      !Array.isArray(base[key])
    ) {
      output[key] = mergeDictionaries(base[key], value);
    } else {
      output[key] = value;
    }
  });
  return output;
}

function getNestedValue(source, path) {
  return path.split(".").reduce((current, part) => current?.[part], source);
}

function interpolate(template, values = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) =>
    values[key] == null ? "" : String(values[key]),
  );
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(
    () => localStorage.getItem(STORAGE_KEY) || "en",
  );
  const activeLanguage = dictionaries[language] ? language : "en";

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, activeLanguage);
    document.documentElement.lang =
      activeLanguage === "zh" ? "zh-CN" : activeLanguage === "zhTW" ? "zh-TW" : activeLanguage;
    document.documentElement.dir = activeLanguage === "ar" ? "rtl" : "ltr";
  }, [activeLanguage]);

  const value = useMemo(() => {
    function t(key, fallbackOrValues, maybeValues) {
      const fallback =
        typeof fallbackOrValues === "string" ? fallbackOrValues : key;
      const values =
        typeof fallbackOrValues === "object" && fallbackOrValues !== null
          ? fallbackOrValues
          : maybeValues;
      const translated =
        getNestedValue(dictionaries[activeLanguage], key) ??
        getNestedValue(dictionaries.en, key) ??
        fallback;
      return interpolate(translated, values);
    }

    return {
      language: activeLanguage,
      languages: [
        { value: "en", label: dictionaries[activeLanguage].language.english },
        { value: "zh", label: dictionaries[activeLanguage].language.chinese },
        { value: "zhTW", label: dictionaries[activeLanguage].language.traditionalChinese },
        { value: "ar", label: dictionaries[activeLanguage].language.arabic },
        { value: "es", label: dictionaries[activeLanguage].language.spanish },
        { value: "id", label: dictionaries[activeLanguage].language.indonesian },
      ],
      setLanguage,
      t,
    };
  }, [activeLanguage]);

  return createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside LanguageProvider");
  }
  return context;
}
